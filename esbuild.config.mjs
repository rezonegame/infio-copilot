// Post-build: replace all import.meta.url and import.meta in output
function replaceImportMetaInOutput() {
	try {
		let code = fs.readFileSync('main.js', 'utf8')
		let modified = false

		// Step 1: Replace import.meta.url with the shimmed variable
		if (code.includes('import.meta.url')) {
			code = code.replace(/import\.meta\.url/g, 'import_meta_url')
			modified = true
		}

		// Step 2: Replace remaining import.meta (not followed by _) with the shim object
		if (code.includes('import.meta')) {
			code = code.replace(/import\.meta/g, 'import_meta')
			modified = true
		}

		if (modified) {
			fs.writeFileSync('main.js', code)
			console.log('âœ… Replaced import.meta references in output')
		}
	} catch (err) {
		console.error('âŒ Failed to replace import.meta:', err)
	}
}

