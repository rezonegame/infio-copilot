// Post-build: replace all import.meta.url and import.meta in output
// Uses self-contained inline expressions that work in ANY scope (main thread, Worker, Node.js)
function replaceImportMetaInOutput() {
	try {
		let code = fs.readFileSync('main.js', 'utf8')
		let modified = false

		// Step 1: Replace import.meta.url with self-contained inline expression
		if (code.includes('import.meta.url')) {
			code = code.replace(/import\.meta\.url/g, IMPORT_META_URL_EXPR)
			modified = true
		}

		// Step 2: Replace remaining import.meta with self-contained inline object
		if (code.includes('import.meta')) {
			code = code.replace(/import\.meta/g, IMPORT_META_EXPR)
			modified = true
		}

		if (modified) {
			fs.writeFileSync('main.js', code)
			console.log('‚úÖ Replaced import.meta references in output')
		}
	} catch (err) {
		console.error('‚ùå Failed to replace import.meta:', err)
	}
}

